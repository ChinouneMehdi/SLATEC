  !     .. External Functions ..
  REAL, EXTERNAL :: ATAN
  !     .. External Subroutines ..
  EXTERNAL :: XERMSG
  !     .. Intrinsic Functions ..
  INTRINSIC SIGN
  !     .. Data statements ..
  REAL, PARAMETER :: pi = 3.14159265358979323846E0
  !* FIRST EXECUTABLE STATEMENT  ATAN2
  IF ( Cs/=0.0E0 ) THEN
    ATAN2 = ATAN(Sn/Cs)
    IF ( Cs<0.0E0 ) ATAN2 = ATAN2 + pi
    IF ( ATAN2>pi ) ATAN2 = ATAN2 - 2.0*pi
  ELSE
    IF ( Sn==0.0E0 ) CALL XERMSG('SLATEC','ATAN2','BOTH ARGUMENTS ARE ZERO',1,2)
    ATAN2 = SIGN(0.5E0*pi,Sn)
  ENDIF
  !
END FUNCTION ATAN2
