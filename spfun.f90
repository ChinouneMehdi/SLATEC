      +(pi8(n)+t*(0.75E0+CSEVL(50.0E0*t**2-1.0E0,atancs,nterms))),X)
  ELSE
    !
    ATAN = conpi8(4) + pi8(4)
    IF ( y<xbig ) ATAN = conpi8(4)&
      + (pi8(4)-(0.75E0+CSEVL(50.0E0/y**2-1.0E0,atancs,nterms))/y)
    ATAN = SIGN(ATAN,X)
  ENDIF
  !
END FUNCTION ATAN
