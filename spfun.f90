  INTEGER, EXTERNAL :: INITS
  !     .. External Subroutines ..
  EXTERNAL :: R9UPAK, XERMSG
  !     .. Data statements ..
  REAL, PARAMETER :: alncs(6) = [ 1.3347199877973882E0, .000693756283284112E0, &
    .000000429340390204E0, .000000000289338477E0, .000000000000205125E0, &
