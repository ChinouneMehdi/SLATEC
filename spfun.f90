  !***
  ! **Description:**
  !
  !- Usage:
  !
  !      REAL ATAN, X, Y
  !      Y = ATAN(X)
  !
  !- Arguments:
  !
  !    X       :IN  This is the argument.  It will not be modified by
  !                 ATAN.
  !
  !- Function Return Values:
  !
  !    ATAN    :    the arctangent.
  !
  !- Description:
  !
  !    ATAN evaluates the arctangent of an argument.
  !
  !    SERIES FOR ATAN    ON THE INTERVAL  0.          TO  4.00000D-02
  !                                        WITH WEIGHTED ERROR   1.00E-17
  !                                         LOG WEIGHTED ERROR  17.00
  !                               SIGNIFICANT FIGURES REQUIRED  16.38
  !                                    DECIMAL PLACES REQUIRED  17.48
  !
  !    XBNDN = TAN((2*N-1)*PI/16.0)
  !
  !    TANP8(N) = TAN(N*PI/8.)
  !
  !    CONPI8(N) + PI8(N) = N*PI/8.0
  !
  !***
  ! **References:**  (NONE)
  !***
  ! **Routines called:**  CSEVL, INITS, R1MACH, SQRT

  !* REVISION HISTORY  (YYMMDD)
  !   780101  DATE WRITTEN
  !   801001  Revisions by W. Fullerton.
  !   900518  Prologue and code revised to conform to 1990 SLATEC
  !           standards.  (SMR)

  !     .. Scalar Arguments ..
  REAL X
  !     .. Local Scalars ..
  REAL sqeps, t, xbig, y
  INTEGER n, nterms
  !     .. External Functions ..
  REAL, EXTERNAL :: CSEVL, R1MACH, SQRT
  INTEGER, EXTERNAL :: INITS
  !     .. Intrinsic Functions ..
  INTRINSIC ABS, SIGN
  !     .. Save statement ..
  SAVE nterms, sqeps, xbig
  !     .. Data statements ..
  REAL, PARAMETER :: atancs(9) = [ .48690110349241406E0, -.006510831636717464E0, &
    .000038345828265245E0, -.000000268722128762E0, .000000002050093098E0, &
    -.000000000016450717E0, .000000000000136509E0, -.000000000000001160E0, &
    .000000000000000010E0 ]
  REAL, PARAMETER :: xbnd1 = .198912367379658006E+0
  REAL, PARAMETER :: xbnd2 = .668178637919298919E+0
  REAL, PARAMETER :: xbnd3 = 1.49660576266548901E+0
  REAL, PARAMETER :: xbnd4 = 5.02733949212584810E+0
  REAL, PARAMETER :: tanp8(3) = [ .414213562373095048E+0, 1.0E0, &
    2.41421356237309504E+0 ]
  REAL, PARAMETER :: conpi8(4) = [ 0.375E0, 0.75E0, 1.125E0, 1.5E0 ]
  REAL, PARAMETER :: pi8(4) = [ 0.176990816987241548E-1, 0.353981633974483096E-1, &
    0.530972450961724644E-1, 0.0707963267948966192E0 ]
  LOGICAL :: first = .TRUE.
  !* FIRST EXECUTABLE STATEMENT  ATAN
  IF ( first ) THEN
    nterms = INITS(atancs,9,0.1*R1MACH(3))
    sqeps = SQRT(6.0E0*R1MACH(3))
    xbig = 1.0E0/R1MACH(3)
    first = .FALSE.
  ENDIF
  !
  y = ABS(X)
  IF ( y<=xbnd1 ) THEN
    !
    ATAN = X
    IF ( y>sqeps ) ATAN = X*(0.75E0+CSEVL(50.0E0*y**2-1.0E0,atancs,nterms))
    !
  ELSEIF ( y<=xbnd4 ) THEN
    !
    n = 1
    IF ( y>xbnd2 ) n = 2
    IF ( y>xbnd3 ) n = 3
    !
    t = (y-tanp8(n))/(1.0E0+y*tanp8(n))
