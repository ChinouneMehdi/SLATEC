  INTEGER nterms
  !     .. External Functions ..
  REAL, EXTERNAL :: CSEVL, R1MACH, SQRT
  INTEGER, EXTERNAL :: INITS
  !     .. External Subroutines ..
  EXTERNAL :: XERMSG
  !     .. Intrinsic Functions ..
  INTRINSIC ABS, SIGN
  !     .. Save statement ..
  SAVE nterms, sqeps
  !     .. Data statements ..
  REAL, PARAMETER :: asincs(20) = [ .10246391753227159E0, .054946487221245833E0, &
    .004080630392544969E0, .000407890068546044E0, .000046985367432203E0, &
    .000005880975813970E0, .000000777323124627E0, .000000106774233400E0, &
    .000000015092399536E0, .000000002180972408E0, .000000000320759842E0, &
