  REAL(8) :: X, y
  INTEGER, SAVE :: ntk1, ntak1, ntak12
  REAL(8), SAVE :: xmin, xsml
  REAL(8), PARAMETER :: bk1cs(16) = [ +.25300227338947770532531120868533D-1, &
