  REAL, EXTERNAL :: R1MACH, SQRT
  !     .. Intrinsic Functions ..
  INTRINSIC ABS, AIMAG, MAX, MIN, REAL
  !     .. Save statement ..
  SAVE sqeps
  !     .. Data statements ..
  LOGICAL :: first = .TRUE.
  !* FIRST EXECUTABLE STATEMENT  CABS
  IF ( first ) sqeps = SQRT(R1MACH(4))
  first = .FALSE.
  !
  x = ABS(REAL(Z))
  y = ABS(AIMAG(Z))
  r1 = MIN(x,y)
  r2 = MAX(x,y)
  !
  CABS = r2
  IF ( r1>r2*sqeps ) CABS = r2*SQRT(1.0E0+(r1/r2)**2)
  !
END FUNCTION CABS
