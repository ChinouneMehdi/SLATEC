    !
    DO iter = 1, niter
      DSQRT = DSQRT + 0.5D0*(y-DSQRT*DSQRT)/DSQRT
    ENDDO
    !
    DSQRT = D9PAK(sqrt2(irem)*DSQRT,ixpnt)
  ELSE
    DSQRT = 0.0D0
    IF ( X<0.0D0 ) CALL XERMSG('SLATEC','DSQRT','X IS NEGATIVE',1,1)
  ENDIF
  !
END FUNCTION DSQRT
