    y = y - ainty2
    !
    noctnt = INT( AMOD(ainty,8.0E0) )
    ifn = MOD((noctnt+1)/2,2)
    IF ( MOD(noctnt,2)==1 ) y = 1.0E0 - y
    z = 2.0E0*y**2 - 1.0E0
    !
    IF ( ifn/=1 ) THEN
      SIN = y*(0.75E0+CSEVL(z,sincs,ntsn))
    ELSE
      SIN = 0.75E0 + CSEVL(z,coscs,ntcs)
    ENDIF
    IF ( noctnt>3 ) SIN = -SIN
    IF ( X<0.0E0 ) SIN = -SIN
  ENDIF
  !
END FUNCTION SIN
