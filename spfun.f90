  IMPLICIT NONE
  !>
  !***
  !  Compute the arctangent.
  !***
  ! **Library:**   FNLIB
  !***
  ! **Category:**  C4A
  !***
  ! **Type:**      SINGLE PRECISION (ATAN-S, DATAN-D)
  !***
  ! **Keywords:**  ARCTANGENT, ATAN, ELEMENTARY FUNCTION, FORTRAN INTRINSIC
