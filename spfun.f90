  !     .. Save statement ..
  SAVE ntsn, ntcs, xmax, xsml, xwarn
  !     .. Data statements ..
  REAL, PARAMETER :: sincs(8) = [ -.008104079085448715E0, -.039144567527081957E0, &
    .000304509420678944E0, -.000001123574976796E0, .000000002414039972E0, &
    -.000000000003391636E0, .000000000000003358E0, -.000000000000000002E0 ]
  REAL, PARAMETER :: coscs(8) = [ .20326382740961603E0, -.14643664439083686E0, &
    .001921449311814646E0, -.000009964968489829E0, .000000027576595607E0, &
    -.000000000047399498E0, .000000000000055495E0, -.000000000000000047E0 ]
  REAL, PARAMETER ::  pi4rec = +.273239544735162686E0
  LOGICAL :: first = .TRUE.
  !* FIRST EXECUTABLE STATEMENT  SIN
  IF ( first ) THEN
    ntsn = INITS(sincs,8,0.1*R1MACH(3))
    ntcs = INITS(coscs,8,0.1*R1MACH(3))
    xsml = SQRT(6.0E0*R1MACH(3))
    xmax = 1.0E0/R1MACH(4)
    xwarn = SQRT(xmax)
    first = .FALSE.
  ENDIF
  !
  y = ABS(X)
  IF ( y>xmax ) CALL XERMSG('SLATEC','SIN',&
    'NO PRECISION BECAUSE ABS(X) IS TOO BIG',2,2)
  IF ( y>xwarn ) CALL XERMSG('SLATEC','SIN',&
    'ANSWER LT HALF PRECISION BECAUSE ABS(X) IS BIG',1,1)
  !
  IF ( y<xsml ) THEN
    SIN = X
  ELSE
    !
    !       CAREFULLY COMPUTE Y * (4/PI)
    !         = (AINT(Y) + REM(Y)) * (1. + PI4REC)
    !         = AINT(Y) + REM(Y) + Y*PI4REC
    !         = AINT(Y) + AINT(REM(Y)+Y*PI4REC) + REM(REM(Y)+Y*PI4REC)
