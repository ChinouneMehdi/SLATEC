REAL(8) FUNCTION DTAN(X)
  IMPLICIT NONE
  !>
  !***
  !  Compute the double precision tangent.
  !***
  ! **Library:**   FNLIB
  !***
  ! **Category:**  C4A
  !***
  ! **Type:**      DOUBLE PRECISION (TAN-S, DTAN-D)
  !***
